#include <iostream>
#include <vector>

using namespace std;

class Graph {
public:
    void dfs(const vector<vector<int>>& adjMatrix, vector<bool>& visited, vector<int>& ans, int node, int depth) {
        if (depth == 0) return;  

        ans.push_back(node);
        visited[node] = true;

        for (int i = 0; i < adjMatrix.size(); i++) {
            if (adjMatrix[node][i] == 1 && !visited[i]) {
                dfs(adjMatrix, visited, ans, i, depth - 1);
            }
        }
    }

    void iterativeDeepeningDFS(const vector<vector<int>>& adjMatrix, int maxDepth) {
        int vertexCount = adjMatrix.size();
        vector<int> ans;
        vector<bool> visited(vertexCount, false);

        for (int depth = 1; depth <= maxDepth; depth++) {
            fill(visited.begin(), visited.end(), false);  
            vector<int> currentDepthAns;
            dfs(adjMatrix, visited, currentDepthAns, 0, depth);

            cout << "Nodes at depth " << depth << ": ";
            for (auto node : currentDepthAns) {
                cout << node << " ";
            }
            cout << endl;
        }
    }
};

